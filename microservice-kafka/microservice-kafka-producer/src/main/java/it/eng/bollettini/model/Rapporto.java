package it.eng.bollettini.model;

public class Rapporto {
	Long numRapp;
	String servInq;
	String descBlsi;
	String servDispo;
	String descBlsb;
	String libSmart;
	String vincFirmo;
	Long dataPer;
	String blocchi;
	
	public Long getNumRapp() {
		return numRapp;
	}
	public void setNumRapp(Long numRapp) {
		this.numRapp = numRapp;
	}
	public String getServInq() {
		return servInq;
	}
	public void setServInq(String servInq) {
		this.servInq = servInq;
	}
	public String getDescBlsi() {
		return descBlsi;
	}
	public void setDescBlsi(String descBlsi) {
		this.descBlsi = descBlsi;
	}
	public String getServDispo() {
		return servDispo;
	}
	public void setServDispo(String servDispo) {
		this.servDispo = servDispo;
	}
	public String getDescBlsb() {
		return descBlsb;
	}
	public void setDescBlsb(String descBlsb) {
		this.descBlsb = descBlsb;
	}
	public String getLibSmart() {
		return libSmart;
	}
	public void setLibSmart(String libSmart) {
		this.libSmart = libSmart;
	}
	public String getVincFirmo() {
		return vincFirmo;
	}
	public void setVincFirmo(String vincFirmo) {
		this.vincFirmo = vincFirmo;
	}
	public Long getDataPer() {
		return dataPer;
	}
	public void setDataPer(Long dataPer) {
		this.dataPer = dataPer;
	}
	public String getBlocchi() {
		return blocchi;
	}
	public void setBlocchi(String blocchi) {
		this.blocchi = blocchi;
	}
}
